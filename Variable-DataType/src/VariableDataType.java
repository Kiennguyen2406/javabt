public class VariableDataType {
    public static void main(String[] args) {
        int i=10;//Khai báo biến là kiểu int có giá trị là 10
        float f=20.5f;
        double d=20.5;
        boolean b=true;
        char c='a'; //Khai báo biến là kiểu char có kí tuwj a
        String s="Hà Nội"; //Khai báo biến là kiểu string có giá tri là Hà Nội
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
    }
}
